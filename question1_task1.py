# Addition

def addition(a, b):
    return a + b


print(addition(10, 5))

# Subtraction


def subtraction(a, b):
    return a - b


print(subtraction(10, 5))

# Multiplication


def multi(a, b):
    return a * b


print(multi(10, 5))

# Division


def divi(a, b):
    return a / b


print(divi(10, 5))

# Modulus


def modu(a, b):
    return a % b


print(modu(10, 5))

# Exponentiation


def expo(a, b):
    return a ** b


print(expo(10, 5))

# Floor division


def fl_divi(a, b):
    return a // b


print(fl_divi(10, 5))
